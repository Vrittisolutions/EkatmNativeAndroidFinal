package com.vritti.vwb.Beans;

import java.io.Serializable;

/**
 * Created by sharvari on 05-Nov-19.
 */

public class BusinessInformation implements Serializable {


    String PKNotificationId,TypeName,TypeCode,NotificationType,TotalNotification,NotRead,ReadNotification;


    public String getPKNotificationId() {
        return PKNotificationId;
    }

    public void setPKNotificationId(String PKNotificationId) {
        this.PKNotificationId = PKNotificationId;
    }

    public String getTypeName() {
        return TypeName;
    }

    public void setTypeName(String typeName) {
        TypeName = typeName;
    }

    public String getTypeCode() {
        return TypeCode;
    }

    public void setTypeCode(String typeCode) {
        TypeCode = typeCode;
    }

    public String getNotificationType() {
        return NotificationType;
    }

    public void setNotificationType(String notificationType) {
        NotificationType = notificationType;
    }

    public String getTotalNotification() {
        return TotalNotification;
    }

    public void setTotalNotification(String totalNotification) {
        TotalNotification = totalNotification;
    }

    public String getNotRead() {
        return NotRead;
    }

    public void setNotRead(String notRead) {
        NotRead = notRead;
    }

    public String getReadNotification() {
        return ReadNotification;
    }

    public void setReadNotification(String readNotification) {
        ReadNotification = readNotification;
    }
}
