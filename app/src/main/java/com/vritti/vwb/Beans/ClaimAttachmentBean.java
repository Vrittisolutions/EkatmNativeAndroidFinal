package com.vritti.vwb.Beans;

public class ClaimAttachmentBean {
	private String fileName, filePath, fileBase64Code;

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getFilePath() {
		return filePath;
	}

	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}

	public String getFileBase64Code() {
		return fileBase64Code;
	}

	public void setFileBase64Code(String fileBase64Code) {
		this.fileBase64Code = fileBase64Code;
	}
}