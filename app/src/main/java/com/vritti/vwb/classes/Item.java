package com.vritti.vwb.classes;

import java.io.Serializable;

/**
 * Created by sharvari on 10-Mar-19.
 */

public class Item implements Serializable {

    public String TypeName,TypeCode,NotRead,ReadNotification,PKNotificationId;

    public String getTypeName() {
        return TypeName;
    }

    public void setTypeName(String typeName) {
        TypeName = typeName;
    }

    public String getTypeCode() {
        return TypeCode;
    }

    public void setTypeCode(String typeCode) {
        TypeCode = typeCode;

    }

    public String getNotRead() {
        return NotRead;
    }

    public void setNotRead(String notRead) {
        NotRead = notRead;
    }

    public String getReadNotification() {
        return ReadNotification;
    }

    public void setReadNotification(String readNotification) {
        ReadNotification = readNotification;
    }

    public String getPKNotificationId() {
        return PKNotificationId;
    }

    public void setPKNotificationId(String PKNotificationId) {
        this.PKNotificationId = PKNotificationId;
    }
}