package com.vritti.sales.beans;

public class CountryBean {
    String PKCountryId;
    String CountryCode;
    String CountryName;
    String IsDeleted;
    String AddedBy;
    String AddedDt;
    String ModifyBy;
    String ModifiedDt;
    String MobileNoDigits;

    public CountryBean(String PKCountryId, String countryName) {
        this.PKCountryId = PKCountryId;
        this.CountryName = countryName;    }

    public String getPKCountryId() {
        return PKCountryId;
    }

    public void setPKCountryId(String PKCountryId) {
        this.PKCountryId = PKCountryId;
    }

    public String getCountryCode() {
        return CountryCode;
    }

    public void setCountryCode(String countryCode) {
        CountryCode = countryCode;
    }

    public String getCountryName() {
        return CountryName;
    }

    public void setCountryName(String countryName) {
        CountryName = countryName;
    }

    public String getIsDeleted() {
        return IsDeleted;
    }

    public void setIsDeleted(String isDeleted) {
        IsDeleted = isDeleted;
    }

    public String getAddedBy() {
        return AddedBy;
    }

    public void setAddedBy(String addedBy) {
        AddedBy = addedBy;
    }

    public String getAddedDt() {
        return AddedDt;
    }

    public void setAddedDt(String addedDt) {
        AddedDt = addedDt;
    }

    public String getModifyBy() {
        return ModifyBy;
    }

    public void setModifyBy(String modifyBy) {
        ModifyBy = modifyBy;
    }

    public String getModifiedDt() {
        return ModifiedDt;
    }

    public void setModifiedDt(String modifiedDt) {
        ModifiedDt = modifiedDt;
    }

    public String getMobileNoDigits() {
        return MobileNoDigits;
    }

    public void setMobileNoDigits(String mobileNoDigits) {
        MobileNoDigits = mobileNoDigits;
    }

    @Override
    public String toString() {
        // return super.toString();
        return this.CountryName;
    }
}
