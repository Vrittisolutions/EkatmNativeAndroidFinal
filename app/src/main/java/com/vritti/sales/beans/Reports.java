public class Reports {

    String Item;
    float Purchasecost, sallingcost, broughtqty, soldqty, profit, remainingstock;

    public String getItem() {
        return Item;
    }

    public void setItem(String item) {
        Item = item;
    }

    public float getPurchasecost() {
        return Purchasecost;
    }

    public void setPurchasecost(float purchasecost) {
        Purchasecost = purchasecost;
    }

    public float getSallingcost() {
        return sallingcost;
    }

    public void setSallingcost(float sallingcost) {
        this.sallingcost = sallingcost;
    }

    public float getBroughtqty() {
        return broughtqty;
    }

    public void setBroughtqty(float broughtqty) {
        this.broughtqty = broughtqty;
    }

    public float getSoldqty() {
        return soldqty;
    }

    public void setSoldqty(float soldqty) {
        this.soldqty = soldqty;
    }

    public float getProfit() {
        return profit;
    }

    public void setProfit(float profit) {
        this.profit = profit;
    }

    public float getRemainingstock() {
        return remainingstock;
    }

    public void setRemainingstock(float remainingstock) {
        this.remainingstock = remainingstock;
    }
}
